package com.example.laylo.Modelss;

public class ProfileModel {
    String text;
    int image;

    public ProfileModel(int image,String text) {
        this.text = text;
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
